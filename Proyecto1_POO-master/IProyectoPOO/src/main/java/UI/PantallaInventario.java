    private void refrescarTabla() {
        // Limpia la tabla
        modeloTabla.setRowCount(0);

        // Recorre los items del inventario y los agrega a la tabla
        for (Map.Entry<String, Item> entry : inventario.getInventario().entrySet()) {
            Item item = entry.getValue();
            Producto p = item.getProducto();

            String preescripcion;
            if (p instanceof Medicamento) {
                preescripcion = ((Medicamento) p).getPreescripcion();
            } else if (p instanceof Insumo) {
                preescripcion = ((Insumo) p).getPreescripcion();
            } else {
                preescripcion = "N/A";
            }

            modeloTabla.addRow(new Object[]{
                item.getCantidad(),
                preescripcion,
                p.getNombre(),
                p.getPresentacion(),
                p.getPrecio()
            });
        }
        jTable1.setAutoResizeMode(JTable.AUTO_RESIZE_ALL_COLUMNS);
        jTable1.setFillsViewportHeight(true);
    }
