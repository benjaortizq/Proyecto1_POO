    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
            int fila = jTable1.getSelectedRow();
    if (fila == -1) {
        JOptionPane.showMessageDialog(this, "Seleccione un producto para editar.", "Error", JOptionPane.WARNING_MESSAGE);
        return;
    }

    String nombre = (String) modeloTabla.getValueAt(fila, 2); // columna "Insumo"
    Item item = inventario.buscarProducto(nombre);

    // Pedimos nuevos valores
    try {
        int nuevaCantidad = Integer.parseInt(JOptionPane.showInputDialog("Cantidad nueva:", item.getCantidad()));
        int nuevoPrecio = Integer.parseInt(JOptionPane.showInputDialog("Precio nuevo:", item.getProducto().getPrecio()));

        item.setCantidad(nuevaCantidad);
        item.getProducto().setPrecio(nuevoPrecio);  // asume que tu Producto permite setPrecio
        refrescarTabla();
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Ingrese valores válidos.", "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
    String nombre = JOptionPane.showInputDialog("Nombre del producto:");
    if (nombre == null || nombre.isEmpty()) return;

    String presentacion = JOptionPane.showInputDialog("Presentación del producto:");
    if (presentacion == null || presentacion.isEmpty()) return;

    String[] opciones = {"Insumo", "Medicamento"};
    int tipo = JOptionPane.showOptionDialog(this, "Tipo de producto:", "Seleccione",
            JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, opciones, opciones[0]);
    if (tipo == -1) return;

    int precio;
    int cantidad;
    try {
        precio = Integer.parseInt(JOptionPane.showInputDialog("Precio del producto:"));
        cantidad = Integer.parseInt(JOptionPane.showInputDialog("Cantidad del producto:"));
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Ingrese valores numéricos válidos.", "Error", JOptionPane.ERROR_MESSAGE);
        return;
    }

    // Creamos el producto según tipo
    Producto prod;
    if (tipo == 0) {
        prod = new Insumo(nombre, presentacion, precio);
    } else {
        prod = new Medicamento(nombre, presentacion, precio);
    }

    // Creamos el Item
    Item nuevoItem = new Item(prod, cantidad);

    // Agregamos al inventario
    inventario.agregarUnItem(nuevoItem);

    // Refrescamos la tabla
    refrescarTabla();
    }//GEN-LAST:event_jButton5ActionPerformed

